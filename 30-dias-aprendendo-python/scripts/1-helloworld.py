# Começando
# 1 Operações básica

print(5 + 2)   # soma(+)
print(3 - 1)   # subtração(-)
print(3 * 1)   # multiplicação(*)
print(3 / 5)   # divisão(/)
print(3 ** 8)  # potência(**)
print(3 % 7)   # módulo(%)
print(3 // 2)  # divisão inteira(//)

# Conhecendo os tipos de dados

print(type(10))                  # Inteiro
print(type(3.14))                # Float
print(type(1 + 3j))              # Complexo
print(type('Asabeneh'))          # String
print(type([1, 2, 3]))           # Lista
print(type({'name':'Asabeneh'})) # Dicionário
print(type({9.8, 3.14, 2.7}))    # Set
print(type((9.8, 3.14, 2.7)))    # Tupla
print(type(3 == 3))              # Booleano
print(type(3 >= 3))              # Booleano